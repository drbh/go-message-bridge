func runPoller() {
	ticker := time.NewTicker(1000 * time.Millisecond)
	quit := make(chan struct{})

	go func() {
		for {
			select {
			case <-ticker.C:
				poll()
			case <-quit:
				ticker.Stop()
				return
			}
		}
	}()
}

